# In Python, a variable is a way to store a value or data.
# It is like a container that holds a value, and you can give it a name.
# You can use this name to refer to the value stored in the variable.

# To create a variable, you need to give it a name and assign a value to it using the equals sign (=)
# For example, let's create a variable called "age" and assign it the value of 20.
age = 20

# Now you can use the variable "age" to refer to the value 20.
print(age)  # Output: 20

# You can also change the value of a variable by assigning a new value to it
age = 50
print(age)  # Output: 50

# Variable names can be a combination of letters, numbers, and underscores. Variable names cannot start with a number.
# Variable names are case-sensitive. For example, x and X are two different variables.

