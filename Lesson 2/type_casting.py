# Let's say we have an integer variable called "profit" and we want to print it with some text.
profit = 100
# print("Total profit: " + profit) This gives us an error because we can only combine strings. We need to convert this integer to a string.

# To cast our integer to a string, we use the following function:
print("Total profit: " + str(profit))

