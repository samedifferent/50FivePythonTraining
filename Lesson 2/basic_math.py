# Python has built-in support for basic math operations.
# You can use the following operators to perform math operations:
# + for addition
# - for subtraction
# * for multiplication
# / for division
# % for modulus (remainder)
# ** for exponentiation

# For example:

# Addition:
x = 5 + 2  # x is now 7
print(x)  # Output: 7

# Subtraction:
x = 5 - 2  # x is now 3
print(x)  # Output: 3

# Multiplication:
x = 5 * 2  # x is now 10
print(x)  # Output: 10

# Division:
x = 5 / 2  # x is now 2.5
print(x)  # Output: 2.5

# Modulus (remainder)
x = 5 % 2  # x is now 1
print(x)  # Output: 1

# Exponentiation
x = 5 ** 2  # x is now 25
print(x)  # Output: 25

# It's also possible to use parentheses to define the order of operations
x = (5 + 2) * 3  # x is now 21
print(x)  # Output: 21

# You can also perform math operations on a variable and assign the result to itself
x = x + 10  # x is now 31
print(x)  # Output: 31

# Python actually has an easier way to do this. You can put the operator before the equals sign, followed by the value you want to operate by.
x += 10  # This is exactly the same as writing x = x + 10
print(x)  # Output: 41
# You can do +=, -=, *=, /=, %=, and **=
